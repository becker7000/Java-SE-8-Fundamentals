public class ComparandoValores {

    public static void main(String[] args) {

        int a = 16;
        int b = 16;

        // a mayor que b
        if(a>b){
            System.out.printf("\n\t a = %d es mayor que b = %d",a,b);
        }

        // b mayor que a, ( a menor que b)
        if(a<b){
            System.out.printf("\n\t b = %d es mayor que a = %d",b,a);
        }

        // a igual b
        if(a==b){
            System.out.printf("\n\t Son iguales");
        }


    }

}
